package com.model;

import java.sql.Timestamp;

public class StaffModel {
    private int staffId;
    private String staffName;
    private String specialization;
    private String qualification;
    private String phone;
    private String email;
    private int experience;
    private String gender;
    private String address;
    private String status; // Active, Inactive, On Leave, Busy
    private Timestamp createdAt;
    
    // Helper property to track workload (number of active/upcoming appointments assigned)
    private int activeAppointmentsCount;

    public int getStaffId() {
        return staffId;
    }

    public void setStaffId(int staffId) {
        this.staffId = staffId;
    }

    public String getStaffName() {
        return staffName;
    }

    public void setStaffName(String staffName) {
        this.staffName = staffName;
    }

    public String getSpecialization() {
        return specialization;
    }

    public void setSpecialization(String specialization) {
        this.specialization = specialization;
    }

    public String getQualification() {
        return qualification;
    }

    public void setQualification(String qualification) {
        this.qualification = qualification;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public int getExperience() {
        return experience;
    }

    public void setExperience(int experience) {
        this.experience = experience;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public Timestamp getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(Timestamp createdAt) {
        this.createdAt = createdAt;
    }

    public int getActiveAppointmentsCount() {
        return activeAppointmentsCount;
    }

    public void setActiveAppointmentsCount(int activeAppointmentsCount) {
        this.activeAppointmentsCount = activeAppointmentsCount;
    }
}
